const numeros = [100, 200, 300, 400, 500, 600];

// a primeira expressão é executada apenas 1 vez
// a segunda expressão é a condição da execução
// a terceira expressão é o incremento ou decremento (ou outra operação) que será realizado após cada it

//   declara      ; enquanto   ; incremente
for (let indice = 0; indice < numeros.length; indice++) {
  console.log(numeros[indice]);
}
